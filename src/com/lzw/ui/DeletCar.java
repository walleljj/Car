    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        String carId = jTextField1.getText();
        CarDaoImpl cd = new CarDaoImpl();
        if(cd.findById(carId)==null||cd.findById(carId).getState().equals("使用中"))
            JOptionPane.showMessageDialog(null,"车辆不存在或状态不支持删除！\n请检查车牌号是否正确");
        else if(cd.delet(carId)==1)
            JOptionPane.showMessageDialog(null,"删除成功！");
        else
            JOptionPane.showMessageDialog(null,"删除失败！");
    }//GEN-LAST:event_jButton1ActionPerformed

