    private void j111KeyTyped(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_j111KeyTyped
        // TODO add your handling code here:
        int temp = evt.getKeyChar();  
                System.out.println(temp);  
                if(temp == 10){//按回车时  
                  
                }else if(temp != 46){   //没有按小数点时  
                    if(temp != 8){  //没有按backspace时  
                        //下面检查是不是在0~9之间；  
                        if(temp > 57){  
                            evt.consume();    //如果不是则消除key事件,也就是按了键盘以后没有反应;  
                        }else if(temp < 48){  
                            evt.consume();  
                        }  
                    }  
                } 
    }//GEN-LAST:event_j111KeyTyped

