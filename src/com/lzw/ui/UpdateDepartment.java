    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
       int id = Integer.parseInt(j111.getText());
       String pass = jTextField2.getText();
       String department = jTextField3.getText();
       String head = jTextField4.getText();
       String phone = jTextField5.getText();
       DeparDaoImpl dd = new DeparDaoImpl();
       Department d = new Department(id,pass,department,head,phone);
       if(dd.findById(id)==null)
           JOptionPane.showMessageDialog(null,"没有此部门！");
       else if(dd.update(d)==1){
           JOptionPane.showMessageDialog(null,"修改成功！");
           this.setVisible(false);}
       else
           JOptionPane.showMessageDialog(null,"修改失败！");
       
    }//GEN-LAST:event_jButton1ActionPerformed

